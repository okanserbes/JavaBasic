package com.neotech.lesson02;

public class VariableDeclaration {

	public static void main(String[] args) {
		// creating and storing values into a variable

		// 1st way. Declaring a variable and assinging a value

		// 1. data type is int
		// 2. name/identifier is number
		// 3. the value that I am storing is 30
		int number = 30;

		// 2nd way.
		short var3; // 1st step is declaring a variable
		var3 = 74; // 2nd step is assigning the value to the variable

		// 3rd way.
		char char1, char2, char3; // Declaring multiple variables
		char1 = 'b';
		char2 = 'j';
		char3 = '$';

		// we are re-assinging a value to the variable
		// we are not declaring a new variable
		var3 = 123;

	}

}
